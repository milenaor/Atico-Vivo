// Función para obtener talento por subcategoría
export function getTalentoBySubcategoria(subcategoria) {
  return talentoData.filter(talento => talento.subcategoria === subcategoria);
}

// Función para buscar talento
export function searchTalento(query) {
  const lowerQuery = query.toLowerCase();
  return talentoData.filter(talento => 
    talento.nombre.toLowerCase().includes(lowerQuery) ||
    talento.descripcion.toLowerCase().includes(lowerQuery)
  );
}

// Función para obtener talento por área
export function getTalentoByArea(area) {
  const areas = {
    'audiovisual': ['imagen', 'produccion', 'postproduccion'],
    'sonido': ['audio', 'musica'],
    'digital': ['interactivo', 'investigacion'],
    'gestion': ['gestion']
  };
  
  const subcategorias = areas[area] || [];
  return talentoData.filter(talento => subcategorias.includes(talento.subcategoria));
}

// Función para obtener talento aleatorio
export function getRandomTalento() {
  return talentoData[Math.floor(Math.random() * talentoData.length)];
}
