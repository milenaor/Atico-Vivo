// Función para obtener proyectos por subcategoría
export function getProyectosBySubcategoria(subcategoria) {
  return proyectosData.filter(proyecto => proyecto.subcategoria === subcategoria);
}

// Función para buscar proyectos
export function searchProyectos(query) {
  const lowerQuery = query.toLowerCase();
  return proyectosData.filter(proyecto => 
    proyecto.nombre.toLowerCase().includes(lowerQuery) ||
    proyecto.descripcion.toLowerCase().includes(lowerQuery)
  );
}

// Función para obtener proyectos que usan un equipo específico
export function getProyectosByEquipo(equipoNombre) {
  return proyectosData.filter(proyecto => 
    proyecto.equipos.some(equipo => equipo.toLowerCase().includes(equipoNombre.toLowerCase()))
  );
}

// Función para obtener proyectos que usan un espacio específico
export function getProyectosByEspacio(espacioNombre) {
  return proyectosData.filter(proyecto => 
    proyecto.laboratorios.some(lab => lab.toLowerCase().includes(espacioNombre.toLowerCase()))
  );
}

// Función para obtener proyectos que requieren un perfil específico
export function getProyectosByPerfil(perfilNombre) {
  return proyectosData.filter(proyecto => 
    proyecto.perfiles.some(perfil => perfil.toLowerCase().includes(perfilNombre.toLowerCase()))
  );
}

// Función para obtener proyecto aleatorio
export function getRandomProyecto() {
  return proyectosData[Math.floor(Math.random() * proyectosData.length)];
}

// Función para obtener recursos necesarios de un proyecto
export function getRecursosProyecto(proyectoId) {
  const proyecto = proyectosData.find(p => p.id === proyectoId);
  if (!proyecto) return null;
  
  return {
    proyecto: proyecto.nombre,
    laboratorios: proyecto.laboratorios,
    equipos: proyecto.equipos,
    perfiles: proyecto.perfiles
  };
}
