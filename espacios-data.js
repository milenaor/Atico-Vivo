// Función para obtener espacios por subcategoría
export function getEspaciosBySubcategoria(subcategoria) {
  return espaciosData.filter(espacio => espacio.subcategoria === subcategoria);
}

// Función para buscar espacios
export function searchEspacios(query) {
  const lowerQuery = query.toLowerCase();
  return espaciosData.filter(espacio => 
    espacio.nombre.toLowerCase().includes(lowerQuery) ||
    espacio.descripcion.toLowerCase().includes(lowerQuery) ||
    espacio.equipamiento?.toLowerCase().includes(lowerQuery)
  );
}

// Función para obtener espacios por capacidad
export function getEspaciosByCapacidad(minCapacidad) {
  return espaciosData.filter(espacio => {
    const capacidad = espacio.capacidad?.toLowerCase();
    return capacidad && (
      capacidad.includes('múltiples') || 
      capacidad.includes('multiusuario') ||
      parseInt(capacidad) >= minCapacidad
    );
  });
}

// Función para obtener espacio aleatorio
export function getRandomEspacio() {
  return espaciosData[Math.floor(Math.random() * espaciosData.length)];
}
